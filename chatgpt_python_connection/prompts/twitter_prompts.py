# Twitter/X Content Prompts

# Twitter Thread Generator
twitter_thread_prompt = """Act as a social media expert.
Give me a 10 tweet thread about [{topic}] in Vietnamese.
The thread should be optimised for virality and contain hashtags and emoticons.
Each tweet should not exceed 280 characters in length."""

# Single Tweet
twitter_single_prompt = """Viết 1 tweet về [{topic}] bằng tiếng Việt.
Yêu cầu:
- Dưới 280 ký tự
- Có emoji
- Có 2-3 hashtags
- Hấp dẫn, viral"""

# Twitter Poll
twitter_poll_prompt = """Tạo một Twitter poll về [{topic}].
Bao gồm:
- Câu hỏi chính (rõ ràng, ngắn gọn)
- 4 lựa chọn trả lời
- Giải thích ngắn tại sao poll này hấp dẫn"""

# Twitter Reply
twitter_reply_prompt = """Viết 3 câu reply thông minh cho tweet về [{topic}].
Reply phải:
- Tạo conversation
- Thêm giá trị
- Ngắn gọn, súc tích"""
